-- group by activity
WITH activity AS (
    SELECT unsub.*,
           GREATEST(ura.last_email_open_tstamp,
                    ura.last_email_click_tstamp,
                    ura.last_pageview_tstamp,
                    ura.last_sale_pageview_tstamp,
                    ura.last_abandoned_booking_tstamp,
                    ura.last_purchase_tstamp,
                    ura.last_complete_booking_tstamp,
                    ura.last_session_end_tstamp) AS most_recent_activity
    FROM archive.sfmc.suppression_list_20211214 unsub
        INNER JOIN data_vault_mvp.dwh.user_recent_activities ura ON unsub.shiro_user_id = ura.shiro_user_id
)
SELECT a.reason_to_unsubscribe,
       CASE
           WHEN DATEDIFF(DAY, a.most_recent_activity, CURRENT_DATE) <= 30 THEN '1. Within 30 days'
           WHEN DATEDIFF(DAY, a.most_recent_activity, CURRENT_DATE) <= 60 THEN '2. Within 60 days'
           WHEN DATEDIFF(DAY, a.most_recent_activity, CURRENT_DATE) <= 365 THEN '3. Within 1 year'
           WHEN DATEDIFF(DAY, a.most_recent_activity, CURRENT_DATE) <= (365 * 4) THEN '4. Within 4 years'
           WHEN DATEDIFF(DAY, a.most_recent_activity, CURRENT_DATE) <= (365 * 7) THEN '5. Within 7 years'
           WHEN a.most_recent_activity IS NULL THEN '6. No recorded activity'
           END AS activity_group,
       COUNT(*)
FROM activity a
GROUP BY 1, 2
;

